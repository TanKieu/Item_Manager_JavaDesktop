    private void btnSaveSuppActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveSuppActionPerformed
        String code= txtSuppCode.getText().trim();
        String name= txtSuppName.getText().trim();
        String address= txtSuppAddr.getText().trim();
        boolean clb= cbColl.isSelected();
        SuppplierDTO sup= new SuppplierDTO(code, name, address, clb);
        if(code.isEmpty()){
            JOptionPane.showMessageDialog(this, "Code is not null!!");
            return;
        }
        if(name.isEmpty()){
            JOptionPane.showMessageDialog(this, "Name is not null!!!");
            return;
        }
        if(addNew){
            try{
                if(!SupplierDao.getSupByCode(code).isEmpty()){
                    JOptionPane.showMessageDialog(this, "Code is duplicated");
                    return;
                }
                if(SupplierDao.insert(sup) ==SUCCESS){
                    Vector row = new Vector();
                    row.add(code);
                    row.add(name);
                    row.add(address);
                    row.add(String.valueOf(clb));
                    Supdata.add(row);
                }
            }catch(Exception ex){
                ex.printStackTrace();
            }
        } else{
            try{
                if(SupplierDao.update(sup)==SUCCESS){
                    int selectedRow= tblSupp.getSelectedRow();
                    Vector row= (Vector) Supdata.get(selectedRow);
                    row.set(1, name);
                    row.set(2, address);
                    row.set(3, clb);
                }
            }catch(Exception ee){
                ee.printStackTrace();
            }
        }
        tblSupp.updateUI();
        loadComboBox();
       
    }//GEN-LAST:event_btnSaveSuppActionPerformed

    private void btnDelSuppActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelSuppActionPerformed

         int r=JOptionPane.showConfirmDialog(this, "Are you sure?", "Remove", JOptionPane.YES_NO_OPTION);
         if(r ==JOptionPane.YES_OPTION){
             try{
                 int selectedRow= tblSupp.getSelectedRow();
                 String suppCode= tblSupp.getValueAt(selectedRow, 0).toString();
                 boolean supplying=Boolean.parseBoolean(tblSupp.getValueAt(selectedRow,3).toString());
                if(supplying==true){
                    JOptionPane.showMessageDialog(this, "Supplier is supplying");
                    return;
                }else{
                    SupplierDao dao= new SupplierDao();
                    if(dao.delete(suppCode)==SUCCESS){
                        Supdata.remove(selectedRow);
                        tblSupp.updateUI();
                    }
                    
                }
                loadComboBox();
                tblItem.updateUI();
                    
             }catch (Exception e){
                  e.printStackTrace();
             }
         }
    }//GEN-LAST:event_btnDelSuppActionPerformed

