    private void btnSaveItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveItemActionPerformed
        String itemCode= txtItemCode.getText().trim();
        String itemName= txtitemName.getText().trim();
        String supplierName= CmbSupplying.getSelectedItem().toString().trim();
        String unit=txtUnit.getText().trim();
        String priceStr=txtPrice.getText().trim();
        boolean supplying= cbSupplier.isSelected(); 
        if(itemCode.isEmpty()){
            JOptionPane.showMessageDialog(this, "Code is not null!!!");
            return;
        }
        if(itemName.isEmpty()){
            JOptionPane.showMessageDialog(this, "Name is not null!!!");
            return;
        }        
        try{
                String supplierCode= SupplierDao.getSupCodeByName(supplierName);
                if(priceStr.matches(".*[a-zA-Z].*")){
                    JOptionPane.showMessageDialog(this, "Price is a positive Number!!!");
                    return;
                }
                if(priceStr.matches(".*-{1,}.*")){
                    JOptionPane.showMessageDialog(this, "Price  is a positive Number!!!");
                    return;
                }
                float price= Float.valueOf(txtPrice.getText().trim());
                ItemDTO item= new ItemDTO(itemCode, itemName, supplierCode, price, unit, supplying);
                if(addNew){
                    try{

                        if(!ItemDao.getItemByCode(itemCode).isEmpty()){
                            JOptionPane.showMessageDialog(this, "Code is duplicated");
                             return;
                        }
                        if(ItemDao.insert(item)==SUCCESS){
                            Vector row = new Vector();
                            row.add(itemCode);
                            row.add(itemName);
                            row.add(supplierCode);
                            row.add(unit);
                            row.add(price);
                            row.add(supplying);
                            ItemData.add(row);
                            tblItem.updateUI();
                        }
                    }catch(Exception ex){
                        ex.printStackTrace();
                    }
                } else{
                    try{
                        if(ItemDao.update(item)==SUCCESS){
                            int selectedRow=tblItem.getSelectedRow();
                            Vector row=(Vector) ItemData.get(selectedRow);
                            row.set(1, itemName);
                            row.set(2,supplierCode);
                            row.set(3, unit);
                            row.set(4, price);
                            row.set(5, supplying);
                            tblItem.updateUI();
                        }
                    } catch(Exception e){
                        e.printStackTrace();
                    }

           
            }
            SupplierDao.checkCollaborating();
            Supdata.clear();
            loadSuppData();
            tblSupp.updateUI();
        }catch(Exception ee){
            ee.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveItemActionPerformed

