    void loadComboBox(){
        CmbSupplying.removeAllItems();
        try {
            for(SuppplierDTO Sup: SupplierDao.getAllSupplier()){
                String box=Sup.getName()+"-"+Sup.getCode();
                CmbSupplying.addItem(box);
             }
        } catch (Exception ex) {
                ex.printStackTrace();
        }
    }
    void loadItemData(){
        try{
           for (ItemDTO i: ItemDao.getAllItem()) {
               Vector row = new Vector();
                String supplying;
                if (i.isSupplying()) {
                    supplying = "true";
                } else {
                    supplying = "false";
                }               
               row.add(i.getCode());
               row.add(i.getName());
               row.add(i.getSupCode());
               row.add(i.getUnit());
               row.add(i.getPrice());
               row.add(supplying);
               ItemData.add(row);
           }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    void loadSuppData(){
        try{
            SupplierDao.checkCollaborating();
            for(SuppplierDTO s : SupplierDao.getAllSupplier()){
                Vector row= new Vector();
                String clb;
                if(s.isColloborate()) clb="true";
                else clb="false";
                row.add(s.getCode());
                row.add(s.getName());
                row.add(s.getAddress());
                row.add(clb);
                Supdata.add(row);
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }

